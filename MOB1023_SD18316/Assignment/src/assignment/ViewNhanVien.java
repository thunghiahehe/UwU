    void fillToTable(List<NhanVien> list) {
        model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        for (NhanVien nhanVien : list) {
            model.addRow(nhanVien.toDataRow());
        }
    }

    void showDetails(int index) {
        NhanVien nv = service.getAll().get(index);
        txtHoTen.setText(nv.getTen());
        txtMaNV.setText(nv.getMa());
        txtLuong.setText(String.valueOf(nv.getLuong()));
        txtTuoi.setText(String.valueOf(nv.getTuoi()));
        txtEmail.setText(nv.getEmail());
    }

    NhanVien readForm() {
        String ma, ten, email;
        double luong;
        int tuoi;
        ma = txtMaNV.getText();
        ten = txtHoTen.getText();
        email = txtEmail.getText();
        luong = Double.parseDouble(txtLuong.getText());
        tuoi = Integer.parseInt(txtTuoi.getText());
        if (tuoi<16||tuoi>65) {
            JOptionPane.showMessageDialog(this, "Tuổi không hợp lệ");
            return null;
        }
        return new NhanVien(ma, ten, tuoi, email, luong);
    }

    void clearForm() {
        txtHoTen.setText("");
        txtMaNV.setText("");
        txtLuong.setText("");
        txtTuoi.setText("");
        txtEmail.setText("");
    }
