    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        index = tblNhanVien.getSelectedRow();
        if (index != -1) {
            JOptionPane.showMessageDialog(this, service.updateNhanVien(index, this.readForm()));
        } else {
            JOptionPane.showMessageDialog(this, service.addNhanVien(this.readForm()));
        }
        this.fillToTable(service.getAll());
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        String ma;
        ma = JOptionPane.showInputDialog(this, "Mời bạn nhập mã nhân viên cần tìm:");
        if (ma.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã không được để trống");
            ma = JOptionPane.showInputDialog(this, "Mời bạn nhập mã nhân viên cần tìm:");
        } else {
            service.findNV(ma);
            this.fillToTable(service.findNV(ma));
        }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenActionPerformed
        // TODO add your handling code here:
        JFileChooser jc = new JFileChooser();
        int choose = jc.showOpenDialog(null);
        if (choose == 0) {
            String path = jc.getSelectedFile().getPath();
            JOptionPane.showMessageDialog(this, service.openFile(path));
            if (service.openFile(path).contains("thành công")) {
                this.fillToTable(service.getAll());
                index = 0;
                tblNhanVien.setRowSelectionInterval(index, index);
                this.showDetails(index);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn file");
        }
    }//GEN-LAST:event_btnOpenActionPerformed

    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        // TODO add your handling code here:
        JFileChooser jc = new JFileChooser();
        int choose = jc.showSaveDialog(null);
        if (choose == 0) {
            String path = jc.getSelectedFile().getPath();
            JOptionPane.showMessageDialog(this, service.saveFile(path));
        } else {
            JOptionPane.showMessageDialog(this, "Bạn không lưu file");
        }
        System.exit(0);
    }//GEN-LAST:event_btnExitActionPerformed

