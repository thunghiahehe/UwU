    void fillToTable(List<KhachHang> list) {
        mol = (DefaultTableModel) tbl_KhachHang.getModel();
        mol.setRowCount(0);
        for (KhachHang khachHang : list) {
            mol.addRow(khachHang.toDataRow());
        }
    }

    void showDetails(int index) {
        KhachHang k = service.getAll().get(index);
        txtMaKhach.setText(k.getMa());
        txtHoten.setText(k.getTen());
        cbo_Quoctich.setSelectedItem(k.getQuocTich());
        if (k.getGioiTinh().equalsIgnoreCase("nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }

    KhachHang readForm() {
        String ma, ten, quocTich, gioiTinh;
        ma = txtMaKhach.getText().trim();
        if (ma.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ma rong");
            return null;
        } else {
            if (ma.matches("[a-zA-Z0-9 ]+")) {
                JOptionPane.showMessageDialog(this, "ten sai dinh dang");
                return null;
            }
        }
        //
        ten = txtHoten.getText().trim();
        if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "ten rong");
            return null;
        } else {
            if (ten.matches("[a-zA-Z ]+")) {
                JOptionPane.showMessageDialog(this, "ten sai dinh dang");
                return null;
            }
        }
        //
        quocTich = cbo_Quoctich.getSelectedItem().toString();
        //
        if (rdoNam.isSelected()) {
            gioiTinh = "nam";
        } else {
            gioiTinh = "nu";
        }
        return new KhachHang(ma, ten, quocTich, gioiTinh);
    }
